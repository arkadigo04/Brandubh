package brandubh.util;

/**
 * Enumera los tipos posibles de celdas en el tablero del juego.
 * Cada tipo de celda representa una característica específica en el juego.
 *
 * @author Arkaitz Díez y Hector Sánchez
 */
public enum TipoCelda {
    /**
     * Representa la celda que contiene el trono en el tablero.
     */
    TRONO,

    /**
     * Representa una celda de provincia en el tablero.
     */
    PROVINCIA,

    /**
     * Representa una celda normal en el tablero.
     */
    NORMAL;
}